public class hm_05 {
    public static void main(String[] args) {
        // 宣告並初始化兩個整數變數 a 和 b
        int a = 12;
        int b = 7;
        
        // 計算和、差、積、商以及餘數
        int 和 = a + b;
        int 差 = a - b;
        int 積 = a * b;
        int 商 = a / b;
        int 餘數 = a % b;

        // 打印結果
        System.out.println("和: "+ 和);
        System.out.println("差: "+ 差);
        System.out.println("積: "+ 積);
        System.out.println("商: "+ 商);
        System.out.println("餘數: "+ 餘數);
    }
}
