public class hm_04 {
    public static void main(String[] args) {
        // 宣告並初始化兩個整數變數 a 和 b
        int a = 10;
        int b = 5;

        // 計算和、差、積和商
        int 和 = a + b;
        int 差 = a - b;
        int 積 = a * b;
        int 商 = a / b;

        // 打印結果
        System.out.println("和: " + 和);
        System.out.println("差: " + 差);
        System.out.println("積: " + 積);
        System.out.println("商: " + 商);
    }
}
