public class hm_01 {
    public static void main(String[] args) { //main()函數，主程式開始
        int age= 25;   // 宣告並初始化整數變數 age
        char initial='A' ;  // 宣告並初始化字元變數 initial
        String name = "Jonh" ;  // 宣告並初始化字串變數 name

        // 使用 System.out.println() 方法打印每個變數的值
        System.out.println("Age: " + age);
        System.out.println("Initial: " + initial);
        System.out.println("Name: " +name);
         
   
    }
}
